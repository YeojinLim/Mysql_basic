CREATE DATABASE lms;

-- student 테이블
-- 기본키 : id
CREATE TABLE student (
    id VARCHAR(20) PRIMARY KEY,
    name VARCHAR(30) NOT NULL,
    address VARCHAR(20)
);

-- attendance 테이블 
-- 기본키 : id + attend_dat 조합
-- 외래키 : id
CREATE TABLE attendance (
    id VARCHAR(20),
    attend_date DATE,
    enter_time TIME NOT NULL,
    leave_time TIME NULL,

    PRIMARY KEY(id, attend_date),
    FOREIGN KEY(id) REFERENCES student(id)
);

